<template>
  <section>
    <div class="blockbuster__container">
      <h2>
        Get it at Blockbuster
      </h2>
      <div class="bx--row">
        <div class="bx--col-lg-8 bx--col-md-4 blockbuster__section-mobile-spacing">
          <p>
            You're only a short drive away from watching a movie, grabbing some
            candy, popcorn, and enjoying a relaxing night with your perfectly
            selected movie. If you need directions, click below for step-by-step
            instructions for how to get to the last Blockbuster in Bend, Oregon.
          </p>
          <cv-loading v-if="userLocationLoading" />
          <AppCta
            v-else
            class="blockbuster__cta blockbuster__cta_light"
            label="Get directions to Blockbuster"
            :url="directionsUrl"
          />
        </div>
        <div class="bx--col-lg-8 bx--col-md-4">
          <div
            v-if="userLocationLoading"
            class="blockbuster-directions-section__loading-container"
          >
            <cv-loading />
            <div>Calculating directions...</div>
          </div>
          <iframe
            v-else
            class="blockbuster-directions-section__map"
            frameborder="0"
            marginheight="0"
            marginwidth="0"
            scrolling="no"
            :src="mapSrc"
          />
        </div>
      </div>
    </div>
  </section>
</template>

<script setup lang="ts">
interface Props {
  userLatitude: number
  userLongitude: number
  userLocationLoaded: boolean
  userLocationLoading: boolean
}

const props = defineProps<Props>()

const destination = encodeURIComponent('Blockbuster Bend Oregon')

const origin = computed(() => {
  if (props.userLocationLoaded) {
    return encodeURIComponent(`${props.userLatitude},${props.userLongitude}`)
  }
  return encodeURIComponent('My location')
})

const mapSrc = computed(() => {
  const key = 'AIzaSyCECZXkg_kMH3Odkh9mWAvKaQ7gexzP3UU'
  return `https://www.google.com/maps/embed/v1/directions?key=${key}&origin=${origin.value}&destination=${destination}`
})

const directionsUrl = computed(() => {
  return `https://www.google.com/maps/dir/?api=1&origin=${origin.value}&destination=${destination}`
})
</script>

